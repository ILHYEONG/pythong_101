#  두 숫자 사이의 차이 계산하기
# 단순 곱셈 프로그램
# 나눗셈과 나머지 계산하기

a = int(input("첫번째 숫자?"))
b = int(input("두째 숫자?"))
minus = a-b
print(f'두수의 차이는 {minus}입니다')

c = int(input("첫번째 숫자?"))
d = int(input("두째 숫자?"))
product = c*d
print(f'두수의 곱은 {product}입니다')

e = int(input("첫번째 숫자?"))
f = int(input("두째 숫자?"))
division = e/f
residual =  e%f
print(f'두수의 나눗셈은 {division}이고 나머지는 {residual}입니다.')