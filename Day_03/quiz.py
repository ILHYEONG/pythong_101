# a,b,c를 입력받아 이차방정식의 양의 해를 도출하는 프로그램(실근이 존재할 경우만)
a=  float(input("이차방정식의 2차항의 계수는? "))
b=  float(input("이차방정식의 1차항의 계수는? "))
c=  float(input("이차방정식의 상수항의 계수는? "))
print(f"이차방정식의 해는 {((-b + (b**2-4*a*c)**0.5))/2*a} 입니다.")