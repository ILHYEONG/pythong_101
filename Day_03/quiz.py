# 한 변의 길이와 높이를 받아 정삼각형 넓이 구하는 프로그램
bottom_line = float(input("한 변의 길이를 입력하세요  "))
height = float(input("높이를 입력하세요  "))
print(f"정삼각형의 넓이는 {0.5*bottom_line*height}입니다.")

# 시간 단위인 초를 입력 받으면 분으로 바꾸는 프로그램
